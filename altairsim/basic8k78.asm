KEYSCAN:
	MVI	C,5		;SCAN INPUT LINE FOR KEYWORDS,
	MOV	D,H		;CONDENSE LINE ON TOP OF SELF
	MOV	E,L
	DCX	H		;bscan -
	PUSH	H
	CALL	SCANNXT		;bscan +
