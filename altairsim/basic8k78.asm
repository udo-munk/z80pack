	MOV	A,M
	CPI	' '
	JZ	KEYSCANH	;DELETE BLANKS
	MOV	B,A
	CPI	'"'
	JZ	KEYSCANI	;SWALLOW WHOLE STRING
	ORA	A
	JZ	KEYSCANX
	CPI	'0'		;NON-KEYWORD
	JC	KEYSCANK
	CPI	'<'		;   SO WE DON'T SCAN
	JC	KEYSCANP
KEYSCANK:
	PUSH	B		;SCAN FOR MATCHING KEYWORD
	PUSH	D
	PUSH	H
	ANI	00FH		;HASH CHARACTER
	MOV	E,A
	MVI	D,0
	LXI	H,KEYWADDS	;ADDRESS C'SPONDING KEYWORDS
	DAD	D
	DAD	D
	MOV	E,M
	INX	H
	MOV	D,M
	XCHG
	JMP	KEYSCANB

