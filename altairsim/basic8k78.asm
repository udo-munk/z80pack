;  Page 53
;

	MOV	A,M		;GET ITS LENGTH
	DCR	D
	SUB	D		;SUBTRACT STARTING POSITION
	JC	MIDSTMXT	;NOTHING TO DO IF BEYOND

;
;  Page 54
;

	CMP	E
	JNC	MIDSTMLM
	MOV	E,A
MIDSTMLM:
	PUSH	B		;SAVE SOURCE ADDRESS
	CALL	LDICBMM		;COMPUTE DESTINATION ADDRESS
	MOV	L,D
	MVI	H,0
	DAD	B
	XCHG
	POP	B
	CALL	COPYSTRG	;COPY STRING
MIDSTMXT:
	LHLD	SCANPTR1
	RET

