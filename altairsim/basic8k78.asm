	CALL	LDMMAC		;MAKE NUMERIC ASSIGNMENT
	POP	D
	POP	H
	RET

STRGUNIQ:
	LHLD	ACCUMLTR	;GET STRING DESCRIPTOR
	XCHG			;IS STRING IN STRING SPACE?
	CALL	STRGTEST
	RNC
	CALL	CMHLLTDE	;VARIABLE REFERENCE?
	CNC	STRGSTOR	;IF SO, MAKE NEW COPY
	RET

