co:	in	10h		;read console status
	ani	2		;if not ready
	jz	co		;keep waiting
	mov	a,c		;get character
	out	11h		;and print it
	ret

cinb:	in	10h		;read console status
	ani	1		;check if character available
	rz			;return if not with carry clear
	in	11h		;get character
	stc			;and set carry flag
	ret

dclr:				;not implemented yet
	ret

xco:				;not implemented yet
	ret

;
;  NON-BLOCKING INPUT
;   CHAR IN AC IF NOT ZERO
;   ZERO SET IF NONE
;
SYSKEYIN:
	push	b
	push	d
	push	h
	call	cinb	;get char
	jnc	syskeynone
	cpi	0
	jz	clearscreen
	cpi	1fh	;us to bring to 14
	jz	gomonitor
syskeyinret:
	pop	h
	pop	d
	pop	b
	ret
syskeynone:
	sub	a	;set zero
	jmp	syskeyinret
clearscreen:
	call	dclr	;clear screen
	jmp	syskeynone
gomonitor:
	rst	1	;about using us (^_)
	nop	
	nop
;
;  SEND AC TO SCREEN
;
SYSDISPL:
	push	psw
	push	b
	push	d

;
; Page 2
;

	push	h
	mov	c,a
	call	co	;c to screen
	lda	p3010	;print on the 3010 if zero
	ana	a	
	cz	xco	;yes print
	pop	h
	pop	d
	pop	b
	pop	psw
	RET
;
;  CHECK FOR BREAK REQUEST
;   SET ZERO TO BREAK
;
SYSBREAK:
	call	syskeyin
	jz	nobreak
	sub	a
	ret
nobreak:
	mvi	a,1
	ora	a
	ret
