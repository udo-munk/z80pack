	CPI	KEYTAB		;TAB OPTION?
	JZ	PRNTOPTN
	CPI	KEYSPC		;SPACE OPTION
	JZ	PRNTOPTN
	PUSH	H
	CPI	','
	JZ	PRNTCOMA
	CPI	';'
	JZ	PRNTSEMI
	POP	B
	CALL	VALEXPR		;bscan expr
	DCX	H		;bscan -
	PUSH	H
	CALL	TYPECHK
	JZ	PRTSTRNG
	CALL	VALSTRGN	;CREATE STRING FROM NUMBER
	LHLD	ACCUMLTR	;VERIFY ROOM ENOUGH ON LINE
	MOV	A,M
	LXI	H,CURSPOS
	ADD	M
	INX	H
	ADD	M
	CC	PRNTCRLF	;NO ROOM, FIND ANOTHER LINE
