	XTHL
	CALL	SCANNXTV	;bscan (val)
	DB	KEYTO		;SCAN LIMIT VALUE,
	CALL	VALNUMBR	;bscan numbr
	PUSH	H
	CALL	LDRGAC
	POP	H
	PUSH	B		;SAVE ON STACK
	PUSH	D
	LXI	B,08100H	;LOAD DEFAULT STEP=1.0
	MOV	D,C
	MOV	E,D
	MOV	A,M
	CPI	KEYSTEP		;CHECK FOR EXPLICIT STEP SIZE
	MVI	A,001H
	JNZ	FORSTMST
