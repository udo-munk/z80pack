	XCHG			;MAKE THE ASSIGMENT
	PUSH	D		;SAVE SCAN
	PUSH	H		;SAVE VARIABLE
	CALL	COERCE
	JNZ	LETSTMNM
	CALL	STRGUNIQ	;REMOVE CONFLICT PROBLEMS
	CALL	STRGRELT	;RELEASE STRING TEMPORARY
	POP	H		;COPY DESCRIPTOR TO DESTINATION
	CALL	COPYVAL
	POP	H
	RET

